int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb == 1 || nb == 0)
		return (nb);
	while (i <= nb / 2)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
